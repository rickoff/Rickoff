Normalize.stripChars = function(str)

	local normalisedString = str
	local numberLen = string.len(str)

	for x = 1, numberLen do
		lettreCible = string.sub(str, x, x)
		Byte = string.byte(lettreCible)	
		if tableAccents[Byte] then
			normalisedString = string.gsub(normalisedString, lettreCible, tableAccents[Byte])
			tes3mp.LogMessage(enumerations.log.ERROR, "NORMALIZE : "..normalisedString)
		end
	end
	
	return normalisedString

end

